        ## DATA TYPES

# String (str): A sequence of characters enclosed in quotes.
name = "Alice"
print(name)

# Integer (int): Whole numbers without a decimal point.
age = 25
print(age)

# Float (float): Numbers with a decimal point.
height = 5.9
print(height)

# Boolean (bool): Represents True or False.
is_student = True
print(is_student)


