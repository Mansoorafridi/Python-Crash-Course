# Logical operators: and, or, not.

a = 5
b = 10
c = 15

# and operator :  and operator give true when both statements are true otherwisw false
print(a < b and b > c)     # False because b < c


# or operator :  or operator gives true when one of the statement is true otherwise false
print(a < b or b > c)      # True because a < b


# not operator : This operator reverse the result, returning false if the result is true
                # and  returning true if the result is false

print(not(a < b))   # False because a < b is True

