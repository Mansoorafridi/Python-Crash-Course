# Assignment operators
#    +=
#    -=
#    *=
#    **=
#    /=
#    //=        Floor division divides the number and round off to the nearest whole number.
#    %=

a = 5
print(a)
a += 2                   # increment the value of 5 by 2 and assign it to a
print(a)

a = 5
print(a)
a -= 2                   # decrement the value of a by 2 and assign it to a
print(a)

a = 5
print(a)
a *= 2                  # Multiply the value of a by 2 and assign it to a
print(a)

a = 5
print(a)
a **= 2                 # Exponent the value of a by 2 and assign it to a
print(a)

a = 5
print(a)
a /= 2                  # Divide the value of a by 2 and assign it to a
print(a)

a = 5
print(a)
a //= 2                 # Floor Divide the value of a by 2 and assign it to a
print(a)

a = 5
print(a)
a %= 2                  # Module give us reminder
print(a)
