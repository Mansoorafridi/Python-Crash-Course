name = "jonathan is good boy\n not a bad boy"      # Newline (\n): Inserts a new line.
print(name)

tabspace = "jonathan is good boy\t not a bad boy"      # Tab (\t): Inserts a tab.
print(tabspace)

single_quote = "jonathan is good boy\' not\' a bad boy"    # Single Quote (\'): Inserts a single quote.
print(single_quote)

double_quote = "jonathan is good boy\" not\" a bad boy"    # Double Quote (\"): Inserts a double quote.

print(double_quote)

print("This is a backslash: \\")       # Backslash (\\): Inserts a backslash.
# Output: This is a backslash: \
