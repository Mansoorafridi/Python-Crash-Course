## For Loop with list
list = [1, 7, 8]
for item in list:
    print(item) # prints 1, 7 and 8


## For Loop with Tuples
t = (5,67,109,44,302)
for i in t:
    print(i)


## For Loop with string
s = "jonathan"
for i in s:
    print(i)

## Range Function
for i in range(4):
    print(i)

##For Loop wirh else statement
l= [2,4,6,8,10]
for el in l:
    print(el)

else:
    print("done") # this is printed when the loop exhausts!